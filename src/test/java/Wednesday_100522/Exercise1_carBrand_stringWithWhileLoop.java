package Wednesday_100522;

public class Exercise1_carBrand_stringWithWhileLoop {
    public static void main(String[] args) {

        String[] carBrand = new String[]{"Honda","Toyota","BMW","Ford","Tesla"};



        int i = 0;

        while(i < carBrand.length){

            System.out.println(carBrand[i]);

            i++;

        }//end of while loop



    }//end of main
}//end of java
